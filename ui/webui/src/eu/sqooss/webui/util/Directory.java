package eu.sqooss.webui.util;

import java.util.ArrayList;
import java.util.List;

public class Directory {
    private Long id = null;
    private String name = null;
    Long parent = null;
    List<Long> childs = new ArrayList<Long>();
    boolean collapsed = true;

    public Directory() {
        super();
    }

    public Directory(Long id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getParent() {
        return parent;
    }

    public void setParent(Long parent) {
        this.parent = parent;
    }

    public boolean addChild(Long child) {
        return childs.add(child);
    }

    public boolean removeChild(Long child) {
        return childs.remove(child);
    }

    public List<Long> getChilds() {
        return childs;
    }

    public boolean isLeaf() {
        return childs.isEmpty();
    }
    
    public boolean isRoot() {
        return (parent == null);
    }

    public boolean isCollapsed() {
        return collapsed;
    }

    public void setCollapsed(boolean collapse) {
        this.collapsed = collapse;
    }

}
